# Los números no pueden cambiar de dato, un 10 siempre es un 10
number_one = 10
number_two = 4

result = number_one + number_two
print ("Suma", result)

result = number_one - number_two
print ("Resta", result)

result = number_one * number_two
print ("Multiplicación", result)

result = number_one / number_two #Regresa un número flotante
print ("División - Devuelve flotante", result)

#Si quiero que la división me devuelva un entero, pongo dos barras //
result = number_one // number_two
print ("División - Devuelve entero", result)

result = number_one ** number_two
print ("Exponencial", result)